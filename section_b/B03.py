# 5つの席から3つの席を選択する組み合わせについて考える
# 12345 の中から3つ選択する組み合わせを網羅するには以下のように考える

# 12を固定して残りを網羅
# 123,124,125
# 13を固定して残りを網羅
# 134,135
# ...
# 34を固定して残りを網羅
# 345

# この網羅方法において1文字目の数字は外側のループの繰り返し回数と対応している
# 外側のループの1回目が終了すると、1を使用した全ての組み合わせを網羅できたことになる
# 外側のループの2回目が終了すると、2を使用した全ての組み合わせを網羅できたことになる
# （2を使用した全ての組み合わせのうち1が含まれる組に関しては1回目のループで既に網羅されている）
# （そのため中央のループの開始値は外側のループに1を加算したものでよい）
# このような操作が繰り返されることでアルゴリズムが実現されている


