n = int(input())
a = list(map(int,input().split()))

ans = "No"

for i in range(n-2):
    for j in range(i+1,n-1):
        for k in range(j+1,n):
            if a[i]+a[j]+a[k] == 1000:
                ans = "Yes"

print(ans)

# ポイント1
# i<j<k となるようにリストの要素を選択することで重複を避けている
# 説明
# 組み合わせを考える際には、ABC、BAC などの順番が違うだけの同じ組み合わせは避けなければならない
# そこで必ずアルファベット順並べるといったルールを課すことで、BAC を排除することができる
# 上記のコードでは必ず i<j<k となるようなコードにすることで重複を排除している
# 例えば、中央のループは必ず外側のループの値に+1をした値からループが始まっている
# 中央のループは外側のループより小さい値を選択できないため i<j<k が成り立っていると言える


